export interface Categorie {
  id: number;
  nom: string;
  description: string | null;
  boutique_id: number;
  created_at: string;
  updated_at: string;
  produits_count?: number;
  produits?: Produit[];
}

export interface Produit {
  id: number;
  nom: string;
  description: string | null;
  prix_vente: string;
  stock: number;
  seuil_alerte: number;
  image: string | null;
  categorie_id: number;
  boutique_id: number;
  actif: boolean;
  created_at: string;
  updated_at: string;
}

export interface CreateCategoriePayload {
  nom: string;
  description?: string;
  boutique_id?: number;
}

export interface UpdateCategoriePayload {
  nom?: string;
  description?: string;
}

