export interface Client {
  id: number;
  nom_complet: string;
  telephone: string;
  adresse: string;
  boutique_id: number;
  created_at: string;
  updated_at: string;
  commandes_count?: number;
}

export interface PaginatedClients {
  current_page: number;
  data: Client[];
  first_page_url: string;
  from: number;
  last_page: number;
  last_page_url: string;
  links: Array<{ url: string | null; label: string; active: boolean }>;
  next_page_url: string | null;
  path: string;
  per_page: number;
  prev_page_url: string | null;
  to: number;
  total: number;
}

export interface CreateClientPayload {
  nom_complet: string;
  telephone: string;
  adresse?: string;
  boutique_id?: number;
}

export interface UpdateClientPayload {
  nom_complet?: string;
  telephone?: string;
  adresse?: string;
}

